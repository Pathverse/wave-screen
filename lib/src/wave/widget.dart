import 'dart:math';

import 'package:flutter/foundation.dart';
import 'package:flutter/widgets.dart';
import 'config.dart';

class WaveWidget extends StatefulWidget {
  final List<WaveLayer> layers;
  final Duration duration;
  final Color? backgroundColor;
  final Gradient? backgroundGradient;
  final DecorationImage? backgroundImage;
  final double? width;
  final double? height;
  final Clip clipBehavior;
  final bool repeat;
  final Widget? child;

  const WaveWidget({
    super.key,
    required this.layers,
    this.duration = const Duration(seconds: 16),
    this.backgroundColor,
    this.backgroundGradient,
    this.backgroundImage,
    this.width,
    this.height,
    this.clipBehavior = Clip.hardEdge,
    this.repeat = true,
    this.child,
  }) : assert(layers.length > 0, 'WaveWidget requires at least one wave layer.');

  @override
  State<WaveWidget> createState() => _WaveWidgetState();
}

class _WaveWidgetState extends State<WaveWidget>
    with SingleTickerProviderStateMixin {
  late final AnimationController _controller;

  @override
  void initState() {
    super.initState();
    _controller = AnimationController(vsync: this, duration: widget.duration);
    _startAnimation();
  }

  @override
  void didUpdateWidget(covariant WaveWidget oldWidget) {
    super.didUpdateWidget(oldWidget);
    if (oldWidget.duration != widget.duration) {
      _controller.duration = widget.duration;
    }
    if (oldWidget.repeat != widget.repeat || oldWidget.duration != widget.duration) {
      _startAnimation();
    }
  }

  void _startAnimation() {
    _controller
      ..stop()
      ..reset();
    if (widget.repeat) {
      _controller.repeat();
      return;
    }
    _controller.forward();
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return LayoutBuilder(
      builder: (context, constraints) {
        final width = widget.width ??
            (constraints.maxWidth.isFinite ? constraints.maxWidth : 320.0);
        final height = widget.height ??
            (constraints.maxHeight.isFinite ? constraints.maxHeight : 240.0);

        return ClipRect(
          clipBehavior: widget.clipBehavior,
          child: DecoratedBox(
            decoration: BoxDecoration(
              color: widget.backgroundColor,
              gradient: widget.backgroundGradient,
              image: widget.backgroundImage,
            ),
            child: SizedBox(
              width: width,
              height: height,
              child: Stack(
                fit: StackFit.expand,
                children: [
                  RepaintBoundary(
                    child: AnimatedBuilder(
                      animation: _controller,
                      builder: (context, _) {
                        return CustomPaint(
                          painter: _WavePainter(
                            layers: widget.layers,
                            progress: _controller.value,
                          ),
                        );
                      },
                    ),
                  ),
                  if (widget.child != null) widget.child!,
                ],
              ),
            ),
          ),
        );
      },
    );
  }
}

class _WavePainter extends CustomPainter {
  final List<WaveLayer> layers;
  final double progress;

  const _WavePainter({required this.layers, required this.progress});

  @override
  void paint(Canvas canvas, Size size) {
    final enableBlurEffects = !kIsWeb;

    for (final layer in layers) {
      final geometry = _buildGeometry(size, layer);
      final paint = Paint()..style = PaintingStyle.fill;
      if (layer.gradient != null) {
        paint.shader = layer.gradient!.createShader(geometry.fillPath.getBounds());
      } else {
        paint.color = layer.color!;
      }
      if (enableBlurEffects && layer.blur != null) {
        paint.maskFilter = layer.blur;
      }
      canvas.drawPath(geometry.fillPath, paint);

      final shadowStroke = (layer.amplitude * 0.18).clamp(3.0, 8.0);
      canvas.save();
      canvas.translate(0, shadowStroke * 0.45);
      canvas.drawPath(
        geometry.crestPath,
        Paint()
          ..style = PaintingStyle.stroke
          ..strokeWidth = shadowStroke
          ..strokeCap = StrokeCap.round
          ..color = const Color(0x22000000)
          ..maskFilter = enableBlurEffects
              ? MaskFilter.blur(BlurStyle.normal, shadowStroke)
              : null,
      );
      canvas.restore();

      canvas.drawPath(
        geometry.crestPath,
        Paint()
          ..style = PaintingStyle.stroke
          ..strokeWidth = (shadowStroke * 0.55).clamp(1.25, 3.5)
          ..strokeCap = StrokeCap.round
          ..color = const Color(0x16FFFFFF),
      );
    }
  }

  _WaveGeometry _buildGeometry(Size size, WaveLayer layer) {
    final fillPath = Path();
    final crestPath = Path();
    final baseline = size.height * layer.heightFactor;
    final step = max(1.0, size.width / 180);
    final startY = _pointY(0, size, layer, baseline);

    fillPath.moveTo(0, size.height);
    fillPath.lineTo(0, startY);
    crestPath.moveTo(0, startY);

    for (double x = step; x <= size.width; x += step) {
      final y = _pointY(x, size, layer, baseline);
      fillPath.lineTo(x, y);
      crestPath.lineTo(x, y);
    }

    fillPath.lineTo(size.width, size.height);
    fillPath.close();
    return _WaveGeometry(fillPath: fillPath, crestPath: crestPath);
  }

  double _pointY(double x, Size size, WaveLayer layer, double baseline) {
    final normalizedX = x / size.width;
    final motionOffset = progress * layer.speed * 1.8;
    final shiftedX = normalizedX + layer.horizontalShift + motionOffset;
    final phase = layer.phaseShift + motionOffset * pi * 0.35;
    final primary = sin((shiftedX * layer.frequency * 2 * pi) + phase);
    final secondary = cos(
      (shiftedX * (layer.frequency * 1.18) * 2 * pi) - phase * 0.42,
    );
    final slopeOffset = (normalizedX - 0.5) * size.height * layer.slope;
    final crestBoost = 1.0 + 0.12 * cos((shiftedX * 2 * pi) - phase * 0.25);
    final wave = primary * 0.94 + secondary * layer.secondaryStrength;
    return baseline + slopeOffset + wave * layer.amplitude * crestBoost;
  }

  @override
  bool shouldRepaint(covariant _WavePainter oldDelegate) {
    return oldDelegate.progress != progress || oldDelegate.layers != layers;
  }
}

class _WaveGeometry {
  final Path fillPath;
  final Path crestPath;

  const _WaveGeometry({required this.fillPath, required this.crestPath});
}
